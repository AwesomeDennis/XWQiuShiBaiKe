#pragma mark Image Manager
+ (BOOL)isExistImage:(NSString*)imageName onLocal:(NSString*)path
{
    NSString* localFilePath = [NSHomeDirectory()stringByAppendingPathComponent:path];
    localFilePath = [localFilePath stringByAppendingPathComponent:imageName];
    NSFileManager* fileManger = [NSFileManager defaultManager];
    return [fileManger fileExistsAtPath:localFilePath];
}

+ (void)saveImageData:(NSData*)imageData withName:(NSString*)imageName onLocal:(NSString*)path
{
    if(imageData && imageName)
    {
        NSFileManager* fileManger = [NSFileManager defaultManager];
        NSString* localFilePath = [NSHomeDirectory()stringByAppendingPathComponent:path];
        if(![fileManger fileExistsAtPath:localFilePath])
        {
            [fileManger createDirectoryAtPath:localFilePath withIntermediateDirectories:YES attributes:nil error:nil];
        }
        localFilePath = [localFilePath stringByAppendingPathComponent:imageName];
        if([fileManger fileExistsAtPath:localFilePath])
        {
            [fileManger removeItemAtPath:localFilePath error:nil];
        }
        [imageData writeToFile:localFilePath atomically:NO];
    }
}

+ (UIImage *)loadImage:(NSString*)imageName onLocal:(NSString*)path
{
    UIImage* image = nil;
    if(imageName){
        NSString* localFilePath = [NSHomeDirectory()stringByAppendingPathComponent:path];
        localFilePath = [localFilePath stringByAppendingPathComponent:imageName];
        NSFileManager* fileManger = [NSFileManager defaultManager];
        if([fileManger fileExistsAtPath:localFilePath])
        {
            image = [UIImage imageWithContentsOfFile:localFilePath];
        }
    }
    return image;
}

